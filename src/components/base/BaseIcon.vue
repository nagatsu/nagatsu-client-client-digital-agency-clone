  if (props.spin) {
    classes.push('animate-spin')
  }
  
  // クリック可能
  if (props.clickable) {
    classes.push('cursor-pointer hover:opacity-75 transition-opacity')
  }
  
  // カスタムクラス
  if (props.class) {
    classes.push(props.class)
  }
  
  return classes.join(' ')
})

// アイコンの属性
const iconAttributes = computed(() => {
  const attrs = {}
  
  if (props.ariaLabel) {
    attrs['aria-label'] = props.ariaLabel
  } else {
    attrs['aria-hidden'] = 'true'
  }
  
  if (props.clickable) {
    attrs.role = 'button'
    attrs.tabindex = '0'
  }
  
  return attrs
})

// クリックハンドラ
const handleClick = (event) => {
  if (props.clickable) {
    emit('click', event)
  }
}
